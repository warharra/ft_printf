#include "../includes/ft_printf.h"

intmax_t		ft_atoi(char *str)
{
	int		sign;
	int		r;

	r = 0;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	sign = (*str == '-') ? -1 : 1;
	(*str == '-' || *str == '+') ? str++ : str;
	while (*str >= '0' && *str <= '9')
		r = r * 10 + *(str++) - '0';
	return (sign * r);
}

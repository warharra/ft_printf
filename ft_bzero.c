#include <strings.h>

void				ft_bzero(void *s, size_t n)
{
	unsigned char	*tmp;
	int				i;

	tmp = s;
	i = 0;
	while (i < (int)n)
	{
		tmp[i] = 0;
		i = i + 1;
	}
}
